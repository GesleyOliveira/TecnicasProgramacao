    private void carregaLista(){
        List<Pessoa> lista= controle.getListaPessoa();
        ListarPessoas.removeAll();
        for(Pessoa var: lista){
            if(var.getClass().equals(Professor.class) && cbox_tipo.equals("Professor")){
                ListarPessoas.add(var.getNome());
            } else if(var.getClass().equals(Administrativo.class) && cbox_tipo.equals("Administrativo")){
                ListarPessoas.add(var.getNome());
            } else if(var.getClass().equals(Colegial.class) && cbox_tipo.equals("Colegial")){
                ListarPessoas.add(var.getNome());
            } else if(var.getClass().equals(Fundamental.class) && cbox_tipo.equals("Fundamental")){
                ListarPessoas.add(var.getNome());
            } else{
                ListarPessoas.add(var.getNome());
            }        
        }
    }
