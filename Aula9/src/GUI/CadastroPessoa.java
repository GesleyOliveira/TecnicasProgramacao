    private void bto_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {                                              
        // TODO add your handling code here:
        EnumGenero genero=EnumGenero.valueOf(cbox_genero.getItemAt(cbox_genero.getSelectedIndex()));
        int resp=JOptionPane.showConfirmDialog(rootPane, "Deseja Cadastrar: " + txtb_nome.getText() + genero);
        
        controle.addPessoa(txtb_nome.getText(),Integer.parseInt(txtb_idade.getText()), genero);
        carregaLista();
        
    }
    
